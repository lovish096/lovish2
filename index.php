 <span class="tim">Tim Hortons is a Canadian multinational fast-food restaurant chain known primarily for its coffee and donuts.
     Founded in 1964 by former NHL player Tim Horton and Jim Charade, the chain has grown to become a significant part
of Canadian culture. <br> <br> <br>
Originally focused on serving coffee and donuts, Tim Hortons has expanded its menu over the years to include sandwiches,
 wraps, soups, and other baked goods. It's also known for its Timbits, which are bite-sized donut holes, and its iconic
 Roll Up the Rim to Win promotion, where customers have the chance to win prizes by rolling up the rim of their coffee cups.<br><br><br>
    
Tim Hortons has a significant presence in Canada and has expanded internationally, with locations in the United States
    and other countries. It's often considered a staple of Canadian life, with many Canadians having fond memories of grabbing
    a coffee or snack from Tim Hortons.</span> <p>
    
